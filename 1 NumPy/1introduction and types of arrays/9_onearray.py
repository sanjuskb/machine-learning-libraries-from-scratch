import numpy as np

# 1d array
ao=np.ones(5) # contains all elements as one
print(ao)
print(ao.dtype)
print()

#2D array
ao=np.ones((5,4)) # contains all elements as one
print(ao)
print(ao.dtype)
print()

#3D array
ao=np.ones((3,4,5)) # contains all elements as one
print(ao)
print(ao.dtype)
print()

